// Include Files
#include <iostream>
using namespace std;

int main() {

    // Variable sections
    int a = 6;
    int b = 3; 

    // Add 'a' and 'b'
    cout << a << " + " << b << " = " << a + b << endl;

    // Subtract 'a' and 'b'
    cout << a << " - " << b << " = " << a - b << endl;

    // Multiply 'a' and 'b'
    cout << a << " x " << b << " = " << a * b << endl;

    // Divide 'a' and 'b'
    cout << a << " / " << b << " = " << a / b << endl;

    return 0;

}